-- Show relative line numbers
vim.opt.number = true
vim.opt.relativenumber = true

-- Show line under cursor
vim.opt.cursorline = true

-- Save undo history to file
vim.opt.undofile = true

-- Set to not show mode, since the statusline shows the mode
vim.opt.showmode = false

-- Ignore casing during search
vim.opt.ignorecase = true

-- Keep the signcolumn on
vim.opt.signcolumn = "no"

-- Options for which directions splits should be opened in
vim.opt.splitright = true
vim.opt.splitbelow = true

-- set the number of lines to keep on screen relative to the cursor
vim.opt.scrolloff = 10
